# Prompt the user for their current age
current_age = int(input("How old are you? "))

# Define the current year and the future year
current_year = 2023
future_year = 2050

# Calculate the number of years until 2050
years_until_2050 = future_year - current_year

# Calculate the user's age in 2050
future_age = current_age + years_until_2050

# Print the result
print(f"In 2050, you will be {future_age} years old.")
